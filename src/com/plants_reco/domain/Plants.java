package com.plants_reco.domain;

public class Plants {
	private Integer id;
	private String plant_name;
	private String other_name;
	private String formal_name;
	private String plant_class;
	private String plant_address;
	private String plant_image;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getPlant_name() {
		return plant_name;
	}
	public void setPlant_name(String plant_name) {
		this.plant_name = plant_name;
	}
	public String getOther_name() {
		return other_name;
	}
	public void setOther_name(String other_name) {
		this.other_name = other_name;
	}
	public String getFormal_name() {
		return formal_name;
	}
	public void setFormal_name(String formal_name) {
		this.formal_name = formal_name;
	}
	public String getPlant_class() {
		return plant_class;
	}
	public void setPlant_class(String plant_class) {
		this.plant_class = plant_class;
	}
	public String getPlant_address() {
		return plant_address;
	}
	public void setPlant_address(String plant_address) {
		this.plant_address = plant_address;
	}
	public String getPlant_image() {
		return plant_image;
	}
	public void setPlant_image(String plant_image) {
		this.plant_image = plant_image;
	}
	
}
